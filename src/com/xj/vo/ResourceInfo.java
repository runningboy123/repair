package com.xj.vo;

public class ResourceInfo
{
	private String id;
	private String name;
	private int num;
	private String info;
	public void setId(String id)
	{
		this.id = id;
	}
	public String getId()
	{
		return id;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getName()
	{
		return name;
	}
	public void setNum(int num)
	{
		this.num = num;
	}
	public int getNum()
	{
		return num;
	}

	public void setInfo(String info)
	{
		this.info = info;
	}
	public String getInfo()
	{
		return this.info;
	}
	
}